// Prompts produced by older versions of getAiPromptForClient that should still be
// treated as auto-managed (safe to overwrite when client_type/subtype changes).
const LEGACY_AUTO_AI_PROMPTS = new Set([
  'You are an assistant that classifies call transcripts for service businesses. Analyze the conversation and determine the caller intent.'
]);

// All current preset outputs flattened into a single Set for fast membership checks.
const KNOWN_AUTO_AI_PROMPTS = (() => {
  const set = new Set(LEGACY_AUTO_AI_PROMPTS);
  for (const group of Object.values(CLIENT_AI_PROMPTS || {})) {
    for (const value of Object.values(group || {})) {
      if (typeof value === 'string') set.add(value);
    }
  }
  return set;
})();

// True when an ai_prompt was generated by us (or is empty) and is therefore
// safe to replace with a freshly-derived preset. Anything else is treated as a
// user customization and preserved.
function isAutoManagedPrompt(prompt) {
  if (prompt == null || prompt === '') return true;
  if (prompt === DEFAULT_AI_PROMPT) return true;
  return KNOWN_AUTO_AI_PROMPTS.has(prompt);
}

// The app uses 'medical | non-medical' as its canonical binary (HIPAA email
// gate, etc.), but CLIENT_AI_PROMPTS uses preset family keys (medical,
// home_service, food_service, other). Map the binary onto the prompt family
// so 'non-medical' clients still get sensible auto-prompts.
const CLIENT_TYPE_TO_PROMPT_FAMILY = {
  medical: 'medical',
  'non-medical': 'home_service',
  home_service: 'home_service',
  food_service: 'food_service',
  other: 'other'
};

// Returns the preset ai_prompt for a given type/subtype, or null when the
// client_type isn't recognized. Never silently falls back to medical-default.
function derivePresetAiPrompt(clientType, clientSubtype) {
  if (!clientType) return null;
  const family = CLIENT_TYPE_TO_PROMPT_FAMILY[clientType];
  if (!family || !CLIENT_AI_PROMPTS?.[family]) return null;
  return getAiPromptForClient(family, clientSubtype || undefined);
}








