async function requireOwnerInviteClient(clientId) {
  const { rows } = await query(
    `SELECT u.id, u.email, u.first_name, u.last_name, u.role,
            cp.onboarding_completed_at, cp.activated_at,
            ba.business_name,
            ${clientLabelSelect()}
       FROM users u
       ${clientLabelJoins()}
      WHERE u.id = $1
      LIMIT 1`,
    [clientId]
  );
  if (!rows.length) return { status: 404, message: 'Client not found' };
  const client = rows[0];
  if (client.role !== 'client') return { status: 400, message: 'Owner invite links are only available for client accounts.' };
  if (!client.onboarding_completed_at) return { status: 400, message: 'Client has not completed onboarding.' };
  return { client };
}

