// Convert lead to active client
router.post('/clients/:leadId/agree-to-service', async (req, res) => {
  const userId = req.portalUserId || req.user.id;
  const leadId = req.params.leadId;
  const { services, source, funnel_data, journey_id } = req.body;
  const funnelData = funnel_data || {};
  // New-patient flag from the convert dialog. Anything other than the explicit
  // string 'new' is treated as an existing patient — the conservative default
  // (no 5-star, no conversion relay) so a stale client or malformed body can't
  // spuriously inflate conversions.
  const isNewPatient = String(req.body?.patient_type || 'existing').toLowerCase() === 'new';

  logEvent('active-clients:agree', 'Received agree-to-service request', {
    userId,
    leadId,
    servicesCount: services?.length,
    source,
    patientType: isNewPatient ? 'new' : 'existing',
    // funnel_data carries caller name/phone/email (PHI) — log only non-sensitive
    // shape/flags, never the raw contact info. (HIPAA: no PHI in logs.)
    funnelDataKeys: Object.keys(funnelData || {}),
    hasCallerName: Boolean(funnelData?.caller_name),
    hasCallerNumber: Boolean(funnelData?.caller_number),
    hasEmail: Boolean(funnelData?.email)
  });

  if (!services || !Array.isArray(services) || services.length === 0) {
    logEvent('active-clients:agree', 'Invalid services array', { userId, leadId, services });
    return res.status(400).json({ message: 'At least one service must be selected' });
  }

  try {
    await ensureJourneyTables();
    const { leadCallKey, leadCallUuid } = await resolveLeadCallLink(userId, leadId);
    const profileRes = await query(
      'SELECT ctm_account_number, ctm_api_key, ctm_api_secret FROM client_profiles WHERE user_id = $1 LIMIT 1',
      [userId]
    );
    const ctmCredentials = resolveCtmCreds(profileRes.rows[0] || null);

    // Extract client info from funnel_data
    const clientName = funnelData.caller_name || 'Unknown';
    const clientPhone = funnelData.caller_number || null;
    const clientEmail = funnelData.email || null;
    const requestedServiceIds = Array.from(
      new Set(
        services
          .map((service) => service?.service_id)
          .filter(Boolean)
      )
    );

    if (!requestedServiceIds.length) {
      return res.status(400).json({ message: 'At least one valid service must be selected' });
    }

    const { rows: ownedServices } = await query(
      'SELECT id FROM services WHERE user_id = $1 AND id = ANY($2::uuid[])',
      [userId, requestedServiceIds]
    );
    if (ownedServices.length !== requestedServiceIds.length) {
      return res.status(400).json({ message: 'One or more selected services are invalid for this client account' });
    }

    // Build attribution list for the client (pre-transaction, read-only)
    const attributionSources = new Set();
    const addSources = (value) => {
      if (!value) return;
      if (Array.isArray(value)) {
        value.forEach((entry) => addSources(entry));
        return;
      }
      String(value)
        .split(',')
        .map((s) => s.trim())
        .filter(Boolean)
        .forEach((s) => attributionSources.add(s));
    };
    addSources(source);
    addSources(funnelData.source);

    if (clientPhone && ctmCredentials.accountId && ctmCredentials.apiKey && ctmCredentials.apiSecret) {
      try {
        const phoneSources = await fetchPhoneInteractionSources(ctmCredentials, clientPhone);
        addSources(phoneSources);
      } catch (err) {
        logEvent('active-clients:agree', 'Failed to load CTM attribution', {
          userId,
          phone: clientPhone,
          error: err.message
        });
      }
    }

    let shouldPostCtmScore = false;
    let activeClientId;
    let existingSourceValue = null;
    let restoreArchivedClient = false;
    let isNewClient = true;
    let attributionList = [];
    const dbClient = await getClient();
    try {
      await dbClient.query('BEGIN');

      // Serialize concurrent agree-to-service requests for the same (owner, phone)
      // pair. FOR UPDATE alone only locks existing rows, so two concurrent requests
      // with no existing row can both insert duplicate active_clients. Advisory
      // lock keys on userId + normalized phone digits and releases on COMMIT/ROLLBACK.
      if (clientPhone) {
        const normalizedPhoneDigits = String(clientPhone).replace(/\D/g, '');
        await dbClient.query(
          `SELECT pg_advisory_xact_lock(hashtext($1::text))`,
          [`agree-to-service:${userId}:${normalizedPhoneDigits}`]
        );
      }

      // Check inside transaction to prevent race condition on concurrent requests
      if (clientPhone) {
        const existingClient = await dbClient.query(
          `SELECT id, source, archived_at
           FROM active_clients
           WHERE owner_user_id = $1
             AND ${buildNormalizedPhoneMatchSql('client_phone', '$2')}
           LIMIT 1
           FOR UPDATE`,
          [userId, clientPhone]
        );
        if (existingClient.rows.length > 0) {
          activeClientId = existingClient.rows[0].id;
          existingSourceValue = existingClient.rows[0].source || null;
          restoreArchivedClient = Boolean(existingClient.rows[0].archived_at);
          isNewClient = false;
          logEvent('active-clients:agree', 'Existing client, adding services', { userId, activeClientId, clientPhone });
        }
      }
      addSources(existingSourceValue);
      attributionList = Array.from(attributionSources);

      if (!activeClientId) {
        // Contact Entity Phase 1: resolve contact_id on the POOL (not this transaction's
        // dbClient) — a statement error inside resolveContact would otherwise abort the
        // whole convert transaction (Postgres 25P02). Separate connection = independent;
        // the contact row autocommits, fine for an append-only identity record.
        const acContactId = await resolveContact({ ownerUserId: userId, phone: clientPhone, email: clientEmail, name: clientName });
        const insertConfig = await activeClientsInsertSql();
        const { rows } = await dbClient.query(
          insertConfig.sql,
          insertConfig.params(userId, clientName, clientPhone, clientEmail, source || null, JSON.stringify(funnelData || {}), req.user.id, acContactId)
        );
        activeClientId = rows[0].id;
        existingSourceValue = rows[0].source || null;
        logEvent('active-clients:agree', 'Created active client', { userId, activeClientId, clientName });
      } else if (restoreArchivedClient) {
        await dbClient.query(
          `UPDATE active_clients
           SET archived_at = NULL,
               updated_at = NOW()
           WHERE id = $1 AND owner_user_id = $2`,
          [activeClientId, userId]
        );
      }

      for (const service of services) {
        const { service_id, agreed_price } = service;
        await dbClient.query(
          `INSERT INTO client_services (active_client_id, service_id, agreed_price) 
           VALUES ($1, $2, $3)`,
          [activeClientId, service_id, agreed_price]
        );
      }

      logEvent('active-clients:agree', 'Services added to client', {
        userId,
        activeClientId,
        serviceCount: services.length
      });

      if (attributionList.length) {
        await dbClient.query('UPDATE active_clients SET source = $1 WHERE id = $2', [attributionList.join(', '), activeClientId]);
      }

      if (journey_id || leadCallKey || leadCallUuid || clientPhone) {
        const journeyConditions = ['owner_user_id = $2', activeOnly()];
        const journeyMatchers = [];
        const journeyParams = [activeClientId, userId];

        if (journey_id) {
          journeyParams.push(journey_id);
          journeyMatchers.push(`id = $${journeyParams.length}`);
        }
        if (leadCallKey) {
          journeyParams.push(leadCallKey);
          journeyMatchers.push(`lead_call_key = $${journeyParams.length}`);
        }
        if (leadCallUuid) {
          journeyParams.push(leadCallUuid);
          journeyMatchers.push(`lead_call_id = $${journeyParams.length}`);
        }
        if (clientPhone) {
          journeyParams.push(clientPhone);
          journeyMatchers.push(buildNormalizedPhoneMatchSql('client_phone', `$${journeyParams.length}`));
        }

        if (journeyMatchers.length) {
          await dbClient.query(
            `UPDATE client_journeys
             SET active_client_id = $1,
                 status = 'active_client',
                 paused = false,
                 next_action_at = NULL,
                 updated_at = NOW()
             WHERE ${journeyConditions.join(' AND ')}
               AND (${journeyMatchers.join(' OR ')})`,
            journeyParams
          );
        }
      }

      if (clientPhone) {
        await dbClient.query(
          `UPDATE call_logs
           SET active_client_id = $1
           WHERE (owner_user_id = $2 OR user_id = $2)
             AND active_client_id IS NULL
             AND RIGHT(REGEXP_REPLACE(from_number, '[^0-9]', '', 'g'), 10) = RIGHT(REGEXP_REPLACE($3, '[^0-9]', '', 'g'), 10)`,
          [activeClientId, userId, clientPhone]
        );

        await dbClient.query(
          `DELETE FROM call_log_tags clt
           USING call_logs cl, lead_tags lt
           WHERE clt.call_id = cl.call_id
             AND clt.tag_id = lt.id
             AND lt.owner_user_id = $1
             AND LOWER(lt.name) = 'in journey'
             AND (cl.owner_user_id = $1 OR cl.user_id = $1)
             AND ${buildNormalizedPhoneMatchSql('cl.from_number', '$2')}`,
          [userId, clientPhone]
        );
      }

      if (isNewPatient && (leadCallKey || leadCallUuid)) {
        const scoredLead = await dbClient.query(
          `UPDATE call_logs
           SET score = 5
           WHERE (call_id = $1 OR id = $2)
             AND (owner_user_id = $3 OR user_id = $3)
           RETURNING call_id`,
          [leadCallKey, leadCallUuid, userId]
        );

        if (scoredLead.rowCount) {
          logEvent('active-clients:agree', 'Applied 5-star score to converted lead', {
            userId,
            leadId: leadCallKey || leadCallUuid
          });
          shouldPostCtmScore = Boolean(leadCallKey && ctmCredentials.accountId && ctmCredentials.apiKey && ctmCredentials.apiSecret);
        }
      }

      await dbClient.query('COMMIT');
    } catch (err) {
      await dbClient.query('ROLLBACK').catch(() => {});
      throw err;
    } finally {
      dbClient.release();
    }

    // Propagate the agreed services onto the contact's append-only services ledger.
    // Best-effort + after-commit on the pool — a failure here must never roll back the
    // conversion (Phase-2 backfill is the safety net). Resolve the contact once (covers
    // both the new-client and existing-client branches above).
    try {
      const { rows: acRows } = await query(
        'SELECT contact_id FROM active_clients WHERE id = $1 AND owner_user_id = $2',
        [activeClientId, userId]
      );
      const contactId = acRows[0]?.contact_id || null;
      if (contactId && Array.isArray(services) && services.length) {
        await appendContactServices({
          contactId,
          ownerUserId: userId,
          services: services.map((s) => ({ service_id: s.service_id })),
          source: 'active_client',
          sourceRefId: activeClientId
        });
      }
    } catch (err) {
      console.error('[active-clients:agree] contact_services propagation', { code: err?.code });
    }

    // Relay new client conversion only for truly new clients — service additions to
    // an existing active_client (including reactivating an archived one) must not
    // re-fire the Purchase/conversion event, which would inflate ad-platform totals.
    if (isNewClient && isNewPatient) {
      try {
        // Pull attribution from the originating lead call_log (if any) so Google
        // Ads offline conversion uploads have a click identifier (gclid/gbraid/wbraid).
        let attribution = null;
        if (leadCallKey || leadCallUuid) {
          try {
            const { rows: leadMeta } = await query(
              `SELECT meta FROM call_logs
                WHERE (call_id = $1 OR id = $2)
                  AND (owner_user_id = $3 OR user_id = $3)
                LIMIT 1`,
              [leadCallKey, leadCallUuid, userId]
            );
            attribution = leadMeta[0]?.meta?.attribution || null;
          } catch {
            attribution = null;
          }
        }

        // Atomic dedupe: advisory lock keyed on activeClientId serializes
        // concurrent senders, lock auto-releases on COMMIT after the relay.
        const relayDbClient = await getClient();
        try {
          await relayDbClient.query('BEGIN');
          await relayDbClient.query(
            `SELECT pg_advisory_xact_lock(hashtext($1::text))`,
            [`tracking:new_client:${activeClientId}`]
          );
          const { rows: alreadySent } = await relayDbClient.query(
            `SELECT 1 FROM tracking_event_log
             WHERE source_id = $1 AND event_name = 'new_client' AND success = true
             LIMIT 1`,
            [activeClientId]
          );
          if (!alreadySent.length) {
            await sendTrackingEvent(userId, 'new_client', 'journey', activeClientId, {
              event_source_url: '',
              value: 1,
              currency: 'USD',
              gclid: attribution?.gclid,
              gbraid: attribution?.gbraid,
              wbraid: attribution?.wbraid,
            });
          }
          await relayDbClient.query('COMMIT');
        } catch (lockErr) {
          await relayDbClient.query('ROLLBACK').catch(() => {});
          throw lockErr;
        } finally {
          relayDbClient.release();
        }
      } catch (relayErr) {
        console.error('[hub:relay]', relayErr.message);
      }
    }

    if (shouldPostCtmScore) {
      try {
        await postSaleToCTM(ctmCredentials, leadCallKey, {
          score: 5,
          conversion: 1,
          value: 0
        });
        logEvent('active-clients:agree', 'Posted 5-star conversion to CTM', {
          userId,
          leadId: leadCallKey
        });
      } catch (ctmErr) {
        logEvent('active-clients:agree', 'Failed to post 5-star conversion to CTM', {
          userId,
          leadId: leadCallKey,
          error: ctmErr.message
        });
      }
    }

    res.json({ success: true, active_client_id: activeClientId });
  } catch (err) {
    console.error('[active-clients:agree]', err);
    logEvent('active-clients:agree', 'Error converting to active client', { error: err.message, userId });
    res.status(500).json({ message: 'Unable to process service agreement' });
  }
});

