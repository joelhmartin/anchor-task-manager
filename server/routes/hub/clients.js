router.post('/clients', isAdminOrEditor, async (req, res) => {
  try {
    const { email, name, role, client_package, businessName, client_type, client_subtype, ai_prompt: bodyAiPrompt, client_group_id, isExisting } = req.body || {};
    // Derive ai_prompt server-side so callers that hit this endpoint directly
    // (batch scripts, future API clients) get the same auto-generated prompt as
    // admins clicking through the UI. If the caller explicitly sent ai_prompt,
    // respect that. Otherwise derive from client_type/client_subtype when we
    // recognize the type; leave NULL for unknown types (e.g. 'non-medical').
    const initialAiPrompt = (typeof bodyAiPrompt === 'string' && bodyAiPrompt.length)
      ? bodyAiPrompt
      : derivePresetAiPrompt(client_type, client_subtype);
    const allowedRoles = ['client', 'admin', 'team'];
    const requestedRole = allowedRoles.includes(role) ? role : 'client';
    const newRole = requestedRole;
    const normalizedPackage = CLIENT_PACKAGE_OPTIONS.includes(client_package) ? client_package : null;

    // For existing clients, both businessName and email are required
    if (newRole === 'client' && isExisting) {
      if (!businessName) return res.status(400).json({ message: 'Business name is required' });
      if (!email) return res.status(400).json({ message: 'Email is required for existing clients' });
    } else if (newRole === 'client') {
      if (!businessName) return res.status(400).json({ message: 'Business name is required' });
    } else {
      if (!email) return res.status(400).json({ message: 'Email is required' });
    }

    // Determine the user email
    let userEmail;
    if (newRole === 'client' && !isExisting) {
      // New client: generate a placeholder email that will be replaced during onboarding
      userEmail = `pending-${uuidv4()}@placeholder.anchor`;
    } else {
      // Existing client or admin/team: use provided email
      userEmail = email.toLowerCase();
      const normalizedEmail = normalizeEmailForCollision(userEmail);
      const existing = await query(
        `SELECT id, email, first_name, last_name FROM users
         WHERE email = $1
            OR LOWER(SPLIT_PART(SPLIT_PART(email::text, '@', 1), '+', 1) || '@' || SPLIT_PART(email::text, '@', 2)) = $2
         LIMIT 1`,
        [userEmail, normalizedEmail]
      );
      if (existing.rows.length) {
        return res.status(409).json({
          message: 'Email already in use',
          existing_user_id: existing.rows[0].id
        });
      }
    }

    const [first, ...rest] = (name || '').trim().split(' ').filter(Boolean);
    const last = rest.join(' ');
    const password = uuidv4();
    const hash = await bcrypt.hash(password, 12);

    // For new clients (placeholder email), use businessName as the temporary display name
    // until onboarding collects the owner's real name. For existing clients and staff,
    // use the provided owner name.
    const isNewClientWithoutOwner = newRole === 'client' && !isExisting;
    const firstName = isNewClientWithoutOwner ? businessName : (first || userEmail.split('@')[0]);
    const lastName = isNewClientWithoutOwner ? '' : (last || '');

    const inserted = await query(
      `INSERT INTO users (first_name, last_name, email, password_hash, role)
       VALUES ($1,$2,$3,$4,$5) RETURNING id, first_name, last_name, email, role`,
      [firstName, lastName, userEmail, hash, newRole]
    );

    // For existing clients, skip the wizard and make the account active immediately.
    // The owner invite is only used to let the invited owner claim the account and
    // set a password; it is no longer the gate that activates the account.
    const isExistingClient = newRole === 'client' && isExisting;
    await query(
      `INSERT INTO client_profiles (user_id, client_package, client_identifier_value, client_type, client_subtype, ai_prompt, client_group_id${isExistingClient ? ', onboarding_completed_at, activated_at' : ''})
       VALUES ($1, $2, $3, $4, $5, $6, $7${isExistingClient ? ', NOW(), NOW()' : ''})
       ON CONFLICT (user_id)
       DO UPDATE SET
         client_package = COALESCE(EXCLUDED.client_package, client_profiles.client_package),
         client_identifier_value = COALESCE(EXCLUDED.client_identifier_value, client_profiles.client_identifier_value),
         client_type = COALESCE(EXCLUDED.client_type, client_profiles.client_type),
         client_subtype = COALESCE(EXCLUDED.client_subtype, client_profiles.client_subtype),
         ai_prompt = COALESCE(EXCLUDED.ai_prompt, client_profiles.ai_prompt),
         client_group_id = EXCLUDED.client_group_id,
         ${isExistingClient ? 'onboarding_completed_at = COALESCE(client_profiles.onboarding_completed_at, NOW()),' : ''}
         ${isExistingClient ? 'activated_at = COALESCE(client_profiles.activated_at, NOW()),' : ''}
         updated_at = NOW()`,
      [inserted.rows[0].id, newRole === 'client' ? normalizedPackage : null, newRole === 'client' ? businessName : null, client_type || null, client_subtype || null, initialAiPrompt || null, client_group_id || null]
    );

    // Seed brand_assets.business_name only for the new-client (wizard) path. For
    // existing clients, businessName is just the informal alias — the official
    // business name is set later via brand assets, so we don't seed it here.
    if (newRole === 'client' && !isExisting && businessName) {
      await query(
        `INSERT INTO brand_assets (user_id, business_name)
         VALUES ($1, $2)
         ON CONFLICT (user_id) DO UPDATE SET business_name = COALESCE(brand_assets.business_name, EXCLUDED.business_name)`,
        [inserted.rows[0].id, businessName]
      );
    }

    // Ensure self-owner membership row exists for account visibility
    if (newRole === 'client') {
      await query(
        `INSERT INTO client_account_members (client_owner_id, member_user_id, role, status, accepted_at)
         VALUES ($1, $1, 'owner', 'active', NOW())
         ON CONFLICT (client_owner_id, member_user_id) DO NOTHING`,
        [inserted.rows[0].id]
      );
    }

    // For existing clients, generate a self-scoped owner invite token so the admin
    // can share the claim link immediately or later.
    let inviteUrl = null;
    if (isExistingClient) {
      const rawToken = crypto.randomBytes(32).toString('hex');
      const tokenHash = hashInviteToken(rawToken);
      const expiresAt = INVITE_NEVER_EXPIRES_AT;
      const newUserId = inserted.rows[0].id;

      await query(
        `INSERT INTO client_user_invite_tokens
         (client_owner_id, token_hash, token_value, invite_email, invite_first_name, invite_role, invited_by, expires_at, metadata)
         VALUES ($1, $2, $3, $4, $5, 'owner', $6, $7, $8)`,
        [
          newUserId,
          tokenHash,
          rawToken,
          userEmail,
          firstName || null,
          req.user.id,
          expiresAt,
          JSON.stringify({ self_invite: true, existing_client: true })
        ]
      );

      const baseUrl = resolveAdminBaseUrl(req);
      inviteUrl = `${baseUrl}/accept-invite/${rawToken}`;
      logEvent('clients:activation-link', 'Owner invite token generated for existing client', {
        clientId: newUserId,
        createdBy: req.user.id
      });
    }

    // Log client creation activity
    await logClientActivity({
      userId: req.user.id,
      actionType: ActivityEventTypes.CREATE_CLIENT,
      targetUserId: inserted.rows[0].id,
      ipAddress: req.ip,
      userAgent: req.headers['user-agent'],
      details: { clientEmail: inserted.rows[0].email, role: newRole, isExisting: !!isExisting }
    });

    // For existing clients, return the active state immediately while preserving
    // the owner-invite flag so the hub can keep offering the claim link.
    const clientData = { ...inserted.rows[0] };
    if (isExistingClient) {
      const activatedAt = new Date().toISOString();
      clientData.onboarding_completed_at = activatedAt;
      clientData.activated_at = activatedAt;
      clientData.onboarding_link_status = 'none';
      clientData.has_pending_self_invite = true;
    }

    res.status(201).json({ client: clientData, created: true, isExisting: !!isExisting, inviteUrl });
  } catch (err) {
    console.error('[clients:create]', err);
    res.status(500).json({ message: 'Unable to save client' });
  }
});

